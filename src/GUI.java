        // Insidi bets
        straightHandler();
        splitHandler();
        streetHandler();
        cornerHandler();
        basketHandler();
        doubleStreetHandler();
        
        // Outsided bets
        firstDozenHandler();
        secondDozenHandler();
        thirdDozenHandler();
        firstColumnHandler();
        secondColumnHandler();
        thirdColumnHandler();
        firstHalfHandler();
        secondHalfHandler();
        evenHandler();
        oddHandler();
        redHandler();
        blackHandler();
        
